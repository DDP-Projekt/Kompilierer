#include "ddptypes.h"
#include "math.h"

ddpfloat Sinus(ddpfloat f) {
	return sin(f);
}

ddpfloat Kosius(ddpfloat f) {
	return cos(f);
}

ddpfloat Tangens(ddpfloat f) {
	return tan(f);
}

ddpfloat Arkussinus(ddpfloat f) {
	return asin(f);
}

ddpfloat Arkuskosinus(ddpfloat f) {
	return acos(f);
}

ddpfloat Arkustangens(ddpfloat f) {
	return atan(f);
}

ddpfloat Hyperbelsinus(ddpfloat f) {
	return sinh(f);
}

ddpfloat Hyperbelkosinus(ddpfloat f) {
	return cosh(f);
}

ddpfloat Hyperbeltangens(ddpfloat f) {
	return tanh(f);
}